day = "I'm having a great day"

# puts variable to screen
puts "#{day}"

# puts length to screen
puts "#{day.length}"

# puts found character included in variable
puts "#{day.include? "!"} "

# puts reversed variable
puts "#{day.reverse}"

# Other exercises

# puts index of letter to the screen
puts "#{day.index("h")}"

# replaced what's stored in variable
puts "#{day.replace "I'm not having a good day"}"

# puts letter to screen based on index
puts "#{day.byteslice(5)}"
